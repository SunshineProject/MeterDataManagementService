package it.sinergis.sunshine.util;

public class UsagePoint {
	private int idUsagePoint;
	private int idMeterReading;
	private int idReadingType;
	
	private String offering_name;
	private String identifier;
	
	private String dstStartRule;
	
	private String commodity;
	private String accumulationbehaviour;
	private String uom;
	private String timeattribute;
	private String currency;
	
	private String lastObservation;
	
	private int counter = 0;
	
	public int getCounter() {
		return counter;
	}
	
	public void setCounter(int counter) {
		this.counter = counter;
	}
	
	public String getOffering_name() {
		return offering_name;
	}
	
	public void setOffering_name(String offering_name) {
		this.offering_name = offering_name;
	}
	
	public String getIdentifier() {
		return identifier;
	}
	
	public void setIdentifier(String identifier) {
		this.identifier = identifier;
	}
	
	public String getDstStartRule() {
		return dstStartRule;
	}
	
	public void setDstStartRule(String dstStartRule) {
		this.dstStartRule = dstStartRule;
	}
	
	public String getCommodity() {
		return commodity;
	}
	
	public void setCommodity(String commodity) {
		this.commodity = commodity;
	}
	
	public String getAccumulationbehaviour() {
		return accumulationbehaviour;
	}
	
	public void setAccumulationbehaviour(String accumulationbehaviour) {
		this.accumulationbehaviour = accumulationbehaviour;
	}
	
	public String getUom() {
		return uom;
	}
	
	public void setUom(String uom) {
		this.uom = uom;
	}
	
	public String getTimeattribute() {
		return timeattribute;
	}
	
	public void setTimeattribute(String timeattribute) {
		this.timeattribute = timeattribute;
	}
	
	public String getCurrency() {
		return currency;
	}
	
	public void setCurrency(String currency) {
		this.currency = currency;
	}
	
	public int getIdUsagePoint() {
		return idUsagePoint;
	}
	
	public void setIdUsagePoint(int idUsagePoint) {
		this.idUsagePoint = idUsagePoint;
	}
	
	public int getIdMeterReading() {
		return idMeterReading;
	}
	
	public void setIdMeterReading(int idMeterReading) {
		this.idMeterReading = idMeterReading;
	}
	
	public int getIdReadingType() {
		return idReadingType;
	}
	
	public void setIdReadingType(int idReadingType) {
		this.idReadingType = idReadingType;
	}
	
	public String getLastObservation() {
		return lastObservation;
	}
	
	public void setLastObservation(String lastObservation) {
		this.lastObservation = lastObservation;
	}
}
